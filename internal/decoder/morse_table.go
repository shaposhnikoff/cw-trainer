type Symbol int

const (
	SymDit Symbol = iota
	SymDah
)

func Decode(symbols []Symbol) (rune, bool) {
	key := ""
	for _, s := range symbols {
		if s == SymDit {
			key += "."
		} else {
			key += "-"
		}
	}
	r, ok := morseTable[key]
	return r, ok
}
